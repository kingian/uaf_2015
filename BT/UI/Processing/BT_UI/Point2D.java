public class Point2D
{
    private float _x;
    private float _y;

    public Point2D(float x, float y)
    {
        this._x = x;
        this._y = y;
    }

    public float getX()
    {
        return _x;
    }

    public void setX(float x)
    {
        this._x = x;
    }

    public float getY()
    {
        return _y;
    }

    public void setY(float y)
    {
        this._y = y;
    }
}
